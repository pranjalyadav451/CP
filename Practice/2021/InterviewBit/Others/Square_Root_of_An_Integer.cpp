int sqrt(int A) {
    int left = 1 , right = A, ans = 0 , mid = 0;
    while (left <= right) {
        mid = left + (right - left) / 2;
        if (mid == A / mid) return mid;
        if (mid < A / mid) {
            ans = mid;
            left = mid + 1;
        }
        else {
            right = mid - 1;
        }
    }
    return ans;
}

