int ways(pair<int, int> &size, int len, int width) {
	if (len > size.first or width > size.second) return 0;
	if (len == size.first) return 1;

	int ans = 0;
	ans += ways(size, len + 1, width + 2) + ways(size, len + 2, width + 1);
	return ans;
}

int solve(int A) {
	int len = 3, width = A;
	pair<int, int> size = {len, width};
	int ans = ways(size, 0, 0);
	return ans;
}

