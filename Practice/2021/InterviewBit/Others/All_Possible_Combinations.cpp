vector<string> res;
void ss(vector<string> &A, string &current, int N, pair<int, int> idx) {
	int i = idx.first , j = idx.second;

	if (current.size() > N) return;
	if (current.size() == N) {
		res.push_back(current);
		return;
	}


	for (; i < N; i++) {
		for (; j < A[i].size(); j++) {
			current.push_back(A[i][j]);
			ss(A, current, N, {i + 1, 0});
			current.pop_back();
		}
	}

}

vector<string> specialStrings(vector<string> &A) {
	res.clear();
	string curr;
	ss(A, curr, A.size(), make_pair(0, 0));
	return res;
}



