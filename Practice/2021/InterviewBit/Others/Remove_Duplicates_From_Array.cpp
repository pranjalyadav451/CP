int removeDuplicates(vector<int> &A) {
    int correct_ptr = 0;
    int N = A.size();
    for (int i = 0; i < N; i++) {
        int j = i, current_cnt = 0;
        while (j < N and A[i] == A[j]) {
            current_cnt++;
            if (current_cnt <= 2) {
                A[correct_ptr++] = A[j];
            }
            j++;
        }
        i = j - 1;
    }
    return correct_ptr;
}


